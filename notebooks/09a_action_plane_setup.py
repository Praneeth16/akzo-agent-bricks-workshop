# MAGIC %md
# MAGIC ## DDL — `actions`, `action_events`, `action_policies`
# MAGIC
# MAGIC `actions(id serial pk, agent, action_type, subject, payload jsonb, status, level, region,
# MAGIC requested_by, approved_by, created_at, decided_at, executed_at, result jsonb, external_ref)`;
# MAGIC `action_events(id, action_id fk, ts, event, actor, detail)`;
# MAGIC `action_policies(action_type pk, max_discount_pct, max_spend_eur, allowed_regions text[],
# MAGIC requires_approval bool)`. All idempotent.
# MAGIC
# MAGIC > **Look for:** `Connected as: ('your.email', 'databricks_postgres')` above confirms the write
# MAGIC > identity and database are correct before any DDL runs. Note the design: `action_events` carries
# MAGIC > a foreign key back to `actions`, and the `idx_*` indexes keep the Action Center's status/agent
# MAGIC > queries fast as the audit log grows.

# COMMAND ----------

DDL = [
    f"CREATE SCHEMA IF NOT EXISTS {PG_SCHEMA}",
    """CREATE TABLE IF NOT EXISTS actions (
        id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        agent         TEXT NOT NULL,
        action_type   TEXT NOT NULL,
        subject       TEXT NOT NULL,
        payload       JSONB NOT NULL DEFAULT '{}'::jsonb,
        status        TEXT NOT NULL DEFAULT 'proposed',
        level         INTEGER NOT NULL DEFAULT 2,
        region        TEXT,
        requested_by  TEXT NOT NULL,
        approved_by   TEXT,
        created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
        decided_at    TIMESTAMPTZ,
        executed_at   TIMESTAMPTZ,
        result        JSONB,
        external_ref  TEXT)""",
    """CREATE TABLE IF NOT EXISTS action_events (
        id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        action_id  BIGINT NOT NULL REFERENCES actions(id),
        ts         TIMESTAMPTZ NOT NULL DEFAULT now(),
        event      TEXT NOT NULL,
        actor      TEXT NOT NULL,
        detail     JSONB)""",
    """CREATE TABLE IF NOT EXISTS action_policies (
        action_type       TEXT PRIMARY KEY,
        max_discount_pct  NUMERIC(5,2),
        max_spend_eur     NUMERIC(14,2),
        allowed_regions   TEXT[],
        requires_approval BOOLEAN NOT NULL DEFAULT TRUE)""",
    "CREATE INDEX IF NOT EXISTS idx_actions_status ON actions(status)",
    "CREATE INDEX IF NOT EXISTS idx_actions_agent ON actions(agent)",
    "CREATE INDEX IF NOT EXISTS idx_action_events_action ON action_events(action_id)",
]

with pg() as conn, conn.cursor() as cur:
    for stmt in DDL:
        cur.execute(stmt)
print("DDL applied — actions / action_events / action_policies ready.")

