# MAGIC ## Setup — connect to Lakebase `graphrag-spike`, schema `akzo`
# MAGIC
# MAGIC | Field | Value |
# MAGIC |---|---|
# MAGIC | host | the instance `read_write_dns` |
# MAGIC | port | `5432` |
