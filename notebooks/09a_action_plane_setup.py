# MAGIC %md
# MAGIC ## Seed realistic guardrail policies
# MAGIC
# MAGIC One row per action type the agents can take. Caps are deliberately realistic for the AkzoNobel
# MAGIC Paints/Coatings story so the demo shows both an in-policy execute and a breach→escalate.
# MAGIC `ON CONFLICT` makes the seed idempotent and re-tunable.
# MAGIC
# MAGIC Read each row as a contract the guardrail engine enforces: a `quote_send` over 15% discount or
# MAGIC EUR 250k breaches and must escalate; `crm_task` and `escalation` skip the approval gate
# MAGIC (`requires_approval = False`) because they are low-risk. `allowed_regions` scopes where an action
# MAGIC may run, and `escalation` uses `None` (no region restriction).

# COMMAND ----------

EMEA = ["EMEA", "NL", "DE", "FR", "UK", "BE", "ES", "IT"]

POLICIES = [
    # action_type,        max_discount_pct, max_spend_eur, allowed_regions, requires_approval
    ("quote_send",        15.0,   250000.0,  EMEA,                       True),
    ("price_change",      10.0,   None,      EMEA,                       True),
    ("forecast_override", None,   None,      EMEA,                       True),
    ("scm_reorder",       None,   100000.0,  EMEA,                       True),
    ("scm_reroute",       None,   50000.0,   EMEA,                       True),
    ("crm_task",          None,   None,      EMEA,                       False),
    ("escalation",        None,   None,      None,                       False),
]

UPSERT = """
    INSERT INTO action_policies
        (action_type, max_discount_pct, max_spend_eur, allowed_regions, requires_approval)
    VALUES (%s, %s, %s, %s, %s)
    ON CONFLICT (action_type) DO UPDATE SET
        max_discount_pct  = EXCLUDED.max_discount_pct,
        max_spend_eur     = EXCLUDED.max_spend_eur,
        allowed_regions   = EXCLUDED.allowed_regions,
        requires_approval = EXCLUDED.requires_approval
"""

with pg() as conn, conn.cursor() as cur:
    for p in POLICIES:
        cur.execute(UPSERT, p)
    cur.execute("SELECT action_type, max_discount_pct, max_spend_eur, "
                "allowed_regions, requires_approval FROM action_policies ORDER BY action_type")
    for row in cur.fetchall():
        print(row)

print("Seeded", len(POLICIES), "policies.")

